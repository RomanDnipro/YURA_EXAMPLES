package march31;

public class Holder<T, R, C> {

    T a;
    R b;
    T c;

    public T getA() {
        return a;
    }

    public void setA(T a) {

        this.a = a;
    }

    public R getB() {
        return b;
    }

    public void setB(R b) {
        this.b = b;
    }

    public T getC() {
        return c;
    }

    public void setC(T c) {
        this.c = c;
    }
}
